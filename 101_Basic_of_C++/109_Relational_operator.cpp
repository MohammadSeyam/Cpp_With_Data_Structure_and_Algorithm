#include<iostream>
using namespace std;

int main(){
    int a = 5;
    int b = 7;
    cout<<(a==b)<<endl;     // == -> is equals to
    cout<<(a>b)<<endl;      // >  -> greater than
    cout<<(a<b)<<endl;      // <  -> less than
    cout<<(a!=b)<<endl;     // != -> is not equals to
    cout<<(a>=b)<<endl;     // >= -> greater than or equals to
    cout<<(a<=b)<<endl;     // <= -> less than or equals to 
    return 0;
}